int	ft_is_prime(int nb)
{
	int	i;

	if (nb <= 1)
		return (0);
	i = 1;
	while (i <= nb / i)
	{
		if (nb % i == 0)
		{
			if (i != 1 && i != nb)
				return (0);
		}
		i++;
	}
	return (1);
}
